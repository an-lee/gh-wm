package gen

import (
	"bytes"
	"encoding/json"
	"os"
	"path/filepath"
	"strings"
	"text/template"
)

const wmAgentTpl = `# AUTO-GENERATED by gh-wm. Do not edit manually.
# Regenerate with: gh wm upgrade
name: WM Agent

on:
  issues:
    types: [labeled, opened]
  issue_comment:
    types: [created]
  pull_request:
    types: [opened, synchronize, reopened, review_requested]
  schedule:
{{- range .Schedules }}
    - cron: "{{ . }}"
{{- end }}
  workflow_dispatch:
    inputs:
      issue_number:
        description: Issue or PR number (optional)
        required: false

permissions:
  contents: write
  issues: write
  pull-requests: write

jobs:
  resolve:
    uses: {{ .OwnerRepo }}/.github/workflows/agent-resolve.yml@{{ .Ref }}
    with:
      event_name: {{ "${{" }} github.event_name {{ "}}" }}
      event_json: {{ "${{" }} toJSON(github.event) {{ "}}" }}
      runs_on: '{{ .RunsOnJSON }}'

  run:
    needs: resolve
    if: {{ "${{" }} needs.resolve.outputs.tasks != '[]' {{ "}}" }}
    strategy:
      fail-fast: false
      matrix:
        task: {{ "${{" }} fromJSON(needs.resolve.outputs.tasks) {{ "}}" }}
    uses: {{ .OwnerRepo }}/.github/workflows/agent-run.yml@{{ .Ref }}
    with:
      task_name: {{ "${{" }} matrix.task {{ "}}" }}
      event_name: {{ "${{" }} github.event_name {{ "}}" }}
      event_json: {{ "${{" }} toJSON(github.event) {{ "}}" }}
      runs_on: '{{ .RunsOnJSON }}'
    secrets:
      ANTHROPIC_API_KEY: {{ "${{" }} secrets.ANTHROPIC_API_KEY {{ "}}" }}
`

type wmAgentData struct {
	OwnerRepo  string
	Ref        string
	Schedules  []string
	RunsOnJSON string
}

// WriteWMAgent writes .github/workflows/wm-agent.yml. runsOn is passed to reusable workflows as JSON (see workflow.runs_on in .wm/config.yml); if empty, ["ubuntu-latest"] is used.
func WriteWMAgent(ghWorkflowsDir string, ownerRepo string, schedules []string, runsOn []string) error {
	tpl, err := template.New("wm").Parse(wmAgentTpl)
	if err != nil {
		return err
	}
	labels := runsOn
	if len(labels) == 0 {
		labels = []string{"ubuntu-latest"}
	}
	runsOnJSON, err := json.Marshal(labels)
	if err != nil {
		return err
	}
	data := wmAgentData{
		OwnerRepo:  ownerRepo,
		Ref:        "main",
		Schedules:  dedupe(schedules),
		RunsOnJSON: string(runsOnJSON),
	}
	if len(data.Schedules) == 0 {
		data.Schedules = []string{"0 22 * * 1-5"}
	}
	var buf bytes.Buffer
	if err := tpl.Execute(&buf, data); err != nil {
		return err
	}
	path := filepath.Join(ghWorkflowsDir, "wm-agent.yml")
	return os.WriteFile(path, buf.Bytes(), 0o644)
}

func dedupe(s []string) []string {
	seen := map[string]bool{}
	var out []string
	for _, x := range s {
		x = strings.TrimSpace(x)
		if x == "" || seen[x] {
			continue
		}
		seen[x] = true
		out = append(out, x)
	}
	return out
}
