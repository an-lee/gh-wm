type wmAgentData struct {
	OwnerRepo    string
	Ref          string
	Schedules    []string
	RunsOnJSON   string
	PreStepsYAML string
}

// WriteWMAgent writes .github/workflows/wm-agent.yml. runsOn is passed to reusable workflows as JSON (see workflow.runs_on in .wm/config.yml); if empty, ["ubuntu-latest"] is used.
// When preSteps is non-empty, the run job is generated inline so prerequisite steps (e.g. mise, bundle install) run before gh-wm.
func WriteWMAgent(ghWorkflowsDir string, ownerRepo string, schedules []string, runsOn []string, preSteps []config.StepDef) error {
	labels := runsOn
	if len(labels) == 0 {
		labels = []string{"ubuntu-latest"}
	}
	runsOnJSON, err := json.Marshal(labels)
	if err != nil {
		return err
	}
	data := wmAgentData{
		OwnerRepo:  ownerRepo,
		Ref:        "main",
		Schedules:  dedupe(schedules),
		RunsOnJSON: string(runsOnJSON),
	}
	if len(data.Schedules) == 0 {
		data.Schedules = []string{"0 22 * * 1-5"}
	}

	var tplStr string
	if len(preSteps) > 0 {
		ps, err := renderPreStepsYAML(preSteps)
		if err != nil {
			return err
		}
		data.PreStepsYAML = ps
		tplStr = wmAgentInlineTpl
	} else {
		tplStr = wmAgentTpl
	}

	tpl, err := template.New("wm").Parse(tplStr)
	if err != nil {
		return err
	}
	var buf bytes.Buffer
	if err := tpl.Execute(&buf, data); err != nil {
		return err
	}
	path := filepath.Join(ghWorkflowsDir, "wm-agent.yml")
	return os.WriteFile(path, buf.Bytes(), 0o644)
}

// renderPreStepsYAML marshals pre_steps to indented YAML list items for embedding under jobs.run.steps.
func renderPreStepsYAML(steps []config.StepDef) (string, error) {
	if len(steps) == 0 {
		return "", nil
	}
	b, err := yaml.Marshal(steps)
	if err != nil {
		return "", err
	}
	s := strings.TrimSuffix(string(b), "\n")
	var out strings.Builder
	for _, line := range strings.Split(s, "\n") {
		out.WriteString("      ")
		out.WriteString(line)
		out.WriteString("\n")
	}
	return out.String(), nil
}

func dedupe(s []string) []string {
	seen := map[string]bool{}
	var out []string
	for _, x := range s {
		x = strings.TrimSpace(x)
		if x == "" || seen[x] {
			continue
		}
		seen[x] = true
		out = append(out, x)
	}
	return out
}
